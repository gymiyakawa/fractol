#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(char *s1)
{
	char	*str;
	int		i;

	str = (char *)malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
